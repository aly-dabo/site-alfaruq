          	<p><strong>Biographie</strong></p>
				<p>Al Fàruq est le champion d'Afrique de Slam et champion du Sénégal de Slam Poésie.
				Prix du Public du Grand Slam National de Poésie du Sénégal, cet originaire de la région naturelle de la Casamance a noué son destin avec l'écriture depuis l'école primaire. 
				Ancien membre du prestigieux ''Club des amis du Livre'', il côtoie de nouvelles sensibilités poétiques et devient en 2011 lauréat de la « Caravane des 10 Mots ».

				Auteur du recueil de poésie « Déluge de l'Esprit » publié aux Editions Edilivre à Paris, Al Fàruq se lance dans le Slam en Août 2017. Il devient un des membres fondateurs du plus grand Collectif de Slam sénégalais par le nombre de membres « Parlons Poésie ».
				En octobre 2017, il participe au concours « Slamez pour elles » contre les violences faites aux femmes, organisé par IntraHealth et Vslam, dont il remporte le Prix du Public.

				Fondateur du ''COLLECTIF SLAMANSA'' qui compte près de cent membres actifs, Al Fàruq pilote avec des jeunes slameurs de la Casamance le projet ''Slamons les Consciences'' à travers des ateliers d'écriture qu'il anime dans sa région natale..
				Il a participé au Salon national du livre du Sénégal en 2018, au Salon international du livre d'Alger.
				Par ailleurs, Al Fàruq s'est produit aux côtés d'artistes de renom tels que Baba MAAL, DJ Awadi et ZAO à l'occasion de l'inauguration du Musée des Civilisations Noires.
				Sa poésie lui a valu de travailler avec l'UEMOA pour l'animation d'ateliers d'écriture avec des enfants venus des huit pays membres, de se produire pour l'Agence Universitaire de la Francophonie, pour Africans Rising, pour ne citer que ceux-là.
				Al Fàruq à pris part à de nombreux festivals au Sénégal ainsi qu'à l'extérieur, offrant partout, de sa voix grave, ses poèmes aux cœurs épris de beauté.

				Al Fàruq se définit fièrement comme le ''Slamory Touré'', en référence à l'inspiration qu'il puise de sa culture mandingue.
			    À travers son art, il souhaite « Démocratiser le bonheur ».

